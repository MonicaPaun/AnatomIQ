function loadTranslations(lang) {
  if (!translations[lang]) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
}

// ÃŽncarcÄƒ noile surse video È™i titlurile aferente
function switchLanguage(lang) {
  const videos = document.querySelectorAll('.dynamic-video');
  videos.forEach(video => {
    const newSrc = video.dataset[lang];
    if (newSrc) {
      const source = video.querySelector('source');
      source.src = newSrc;
      video.load(); // ReÃ®ncarcÄƒ video-ul
    }
  });

  const spans = document.querySelectorAll('.overlay span');
  spans.forEach(span => {
    const newText = span.dataset[lang];
    if (newText) {
      span.textContent = newText;
    }
  });
}

