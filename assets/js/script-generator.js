const NR_INTREBARI_TEST = 10;

const form = document.getElementById('testForm');
const submitBtn = document.getElementById('submitBtn');
const newTestBtn = document.getElementById('newTestBtn');
const notaDiv = document.getElementById('nota');

let testCurent = [];

// Funcție pentru a amesteca un array (Fisher-Yates shuffle)
function shuffleArray(array) {
  for(let i = array.length -1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i+1));
    [array[i], array[j]] = [array[j], array[i]];
  }
  return array;
}

// Generează testul cu NR_INTREBARI_TEST întrebări random
function genereazaTest() {
  notaDiv.textContent = '';
  submitBtn.disabled = false;
  newTestBtn.style.display = 'none';
  form.innerHTML = '';

  if(toateIntrebarile.length < NR_INTREBARI_TEST) {
    form.innerHTML = `<p style="color:red; font-weight:bold;">Nu sunt suficiente întrebări în baza de date!</p>`;
    submitBtn.disabled = true;
    return;
  }

  // Amestecăm întrebările și selectăm primele 10
  testCurent = shuffleArray([...toateIntrebarile]).slice(0, NR_INTREBARI_TEST);

  testCurent.forEach((intrebare, index) => {
    const qNum = index + 1;
    const container = document.createElement('div');
    container.className = 'intrebare';

    // Enunțul
    const enunt = document.createElement('div');
    enunt.className = 'enunt';
    enunt.textContent = `${qNum}. ${intrebare.enunt}`;
    container.appendChild(enunt);

    const varianteContainer = document.createElement('div');
    varianteContainer.className = 'variante';

    if(intrebare.tip === 'grila') {
      // Variante grilă - radio buttons
      intrebare.variante.forEach((varianta, i) => {
        const input = document.createElement('input');
        input.type = 'radio';
        input.name = `intrebare_${index}`;
        input.id = `intrebare_${index}_var_${i}`;
        input.value = varianta;

        const label = document.createElement('label');
        label.htmlFor = input.id;
        label.textContent = varianta;

        varianteContainer.appendChild(input);
        varianteContainer.appendChild(label);
      });
    } else if(intrebare.tip === 'completare') {
      // Întrebare de completat - input text
      const input = document.createElement('input');
      input.type = 'text';
      input.name = `intrebare_${index}`;
      input.autocomplete = 'off';
      varianteContainer.appendChild(input);
    }

    container.appendChild(varianteContainer);
    form.appendChild(container);
  });
}

// Verifică răspunsurile și afișează feedback + nota
function verificaRaspunsuri(event) {
  event.preventDefault();

  let punctaj = 0;

  // Pentru fiecare întrebare:
  testCurent.forEach((intrebare, index) => {
    const container = form.children[index];
    const varianteContainer = container.querySelector('.variante');
    let raspunsUtilizator = null;

    if(intrebare.tip === 'grila') {
      const radios = varianteContainer.querySelectorAll(`input[type="radio"]`);
      radios.forEach(radio => {
        if(radio.checked) raspunsUtilizator = radio.value;
      });
    } else if(intrebare.tip === 'completare') {
      const input = varianteContainer.querySelector('input[type="text"]');
      raspunsUtilizator = input.value.trim();
    }

    // Verificare corectitudine
    const corect = raspunsUtilizator !== null &&
      raspunsUtilizator.toLowerCase() === intrebare.raspunsCorect.toLowerCase();

    // Stilizare răspunsuri
    if(intrebare.tip === 'grila') {
      // Pentru grilă colorăm label-ul variantei selectate
      const radios = varianteContainer.querySelectorAll('input[type="radio"]');
      radios.forEach(radio => {
        const label = varianteContainer.querySelector(`label[for="${radio.id}"]`);
        label.classList.remove('corect', 'gresit');
        label.style.pointerEvents = 'none'; // dezactivează hover după submit
        if(radio.checked) {
          if(corect) {
            label.classList.add('corect');
            punctaj++;
          } else {
            label.classList.add('gresit');
          }
        }
        // Afișăm și răspunsul corect, dacă nu l-a ales utilizatorul
        if(radio.value === intrebare.raspunsCorect && !corect) {
          label.classList.add('corect');
        }
      });
    } else if(intrebare.tip === 'completare') {
      const input = varianteContainer.querySelector('input[type="text"]');
      input.disabled = true;
      if(corect) {
        input.classList.add('corect');
        punctaj++;
      } else {
        input.classList.add('gresit');
        // Afișăm răspunsul corect sub input
        let feedback = container.querySelector('.feedback');
        if(!feedback) {
          feedback = document.createElement('div');
          feedback.className = 'feedback';
          container.appendChild(feedback);
        }
        feedback.textContent = `Răspuns corect: ${intrebare.raspunsCorect}. ${intrebare.explicatie || ''}`;
      }
    }

    // Pentru întrebările corecte (grilă) afișăm explicația dedesubt
    if(corect && intrebare.tip === 'grila') {
      let feedback = container.querySelector('.feedback');
      if(!feedback && intrebare.explicatie) {
        feedback = document.createElement('div');
        feedback.className = 'feedback';
        feedback.textContent = intrebare.explicatie;
        container.appendChild(feedback);
      }
    }
  });

  // Dezactivăm butonul submit și inputurile/radio după verificare
  submitBtn.disabled = true;
  newTestBtn.style.display = 'inline-block';

  const inputs = form.querySelectorAll('input');
  inputs.forEach(input => {
    input.disabled = true;
  });

  // Calculăm nota pe 10 și afișăm
  notaDiv.textContent = `Nota ta: ${punctaj} / ${NR_INTREBARI_TEST}`;
}

